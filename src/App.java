// 代码注释
public class App {
    public static void main(String[] args) throws Exception {
        
        System.out.println("Hello, World LeetCode!");
        int[] nums = {3,4,5,1,2};
        

        //for循环  从0开始，到数组长度结束 
        for(int i = 0;i < nums.length;++i){
            System.out.println(nums[i]);
        }
        
    }
}
